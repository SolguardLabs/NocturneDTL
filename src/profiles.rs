pub fn profile_by_code(code: &str) -> Option<&'static WindowProfile> {
    WINDOW_PROFILES.iter().find(|profile| profile.code == code)
}

pub fn profiles_for_batch(class: BatchClass) -> Vec<&'static WindowProfile> {
    WINDOW_PROFILES
        .iter()
        .filter(|profile| profile.recommended_batch == class)
        .collect()
}

pub fn weighted_rollover_budget(class: BatchClass) -> u64 {
    profiles_for_batch(class)
        .into_iter()
        .map(|profile| profile.rollover_grace * u64::from(profile.audit_weight))
        .sum()
}

pub fn conservative_profile(note_count: usize) -> &'static WindowProfile {
    WINDOW_PROFILES
        .iter()
        .find(|profile| profile.accepts_note_count(note_count))
        .unwrap_or(&DEFAULT_PROFILE)
}
