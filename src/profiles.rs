use serde::{Deserialize, Serialize};

#[derive(Copy, Clone, Debug, Serialize, Deserialize, PartialEq, Eq)]
pub enum BatchClass {
    Micro,
    Small,
    Medium,
    Institutional,
    Settlement,
}

#[derive(Copy, Clone, Debug, Serialize, Deserialize)]
pub struct WindowProfile {
    pub code: &'static str,
    pub reserve_floor_bps: u16,
    pub rollover_grace: u64,
    pub max_notes: usize,
    pub withdrawal_delay: u64,
    pub audit_weight: u16,
    pub settlement_label: &'static str,
    pub recommended_batch: BatchClass,
}

impl WindowProfile {
    pub fn accepts_note_count(&self, count: usize) -> bool {
        count <= self.max_notes
    }

    pub fn reserve_ratio(&self) -> f64 {
        f64::from(self.reserve_floor_bps) / 10_000.0
    }

    pub fn class_name(&self) -> &'static str {
        match self.recommended_batch {
            BatchClass::Micro => "micro",
            BatchClass::Small => "small",
            BatchClass::Medium => "medium",
            BatchClass::Institutional => "institutional",
            BatchClass::Settlement => "settlement",
        }
    }
}

